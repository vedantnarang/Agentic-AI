#set operations
set_1={1,2,3,4,5}
set_2={4,5,6,7,8,}
print('union:',set_1|set_2)#unique values 
print('intersection:',set_1&set_2)
print('difference:',set_1-set_2)
print('difference:',set_2-set_1)#unordered
print('symmetric diff:',set_1^set_2)#removes the similar
